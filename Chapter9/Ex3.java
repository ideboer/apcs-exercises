public class Ex3
{
    public static void main ( String[] args )
    {
        double X;

        X = 0.0;
        double value = (3 * Math.pow(X, 2) - (8 * X) + 4 );
        System.out.println("At X = " + X + " the value is " + value);

        X = 2.0;
        value = (3 * Math.pow(X, 2) - (8 * X) + 4 );
        System.out.println("At X = " + X + " the value is " + value);

        X = 4.0;
        value = (3 * Math.pow(X, 2) - (8 * X) + 4 );
        System.out.println("At X = " + X + " the value is " + value);
    }
}